using System;

namespace CourseManagementUITestAutomation.Model
{
    // Represents the model for a department in the Course Management System.
    public class DepartmentModel
    {
        // Gets or sets the name of the department.
        public string Name { get; set; }

        // Gets or sets the budget of the department.
        public string Budget { get; set; }

        // Gets or sets the start date of the department.
        public string StartDate { get; set; }

        // Gets or sets the person ID associated with the department.
        public string PersonId { get; set; }
    }
}
